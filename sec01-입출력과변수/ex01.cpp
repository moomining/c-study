#include <stdio.h>

int main() {
    // %d : 정수 출력 (1,2,3, ..., 0, -1...)
    printf("%d + %d = %d\n", 2, 3, 5);

    // %f : 실수 출력 
    printf("%f\n", 3.14);
    printf("%.2f\n", 3.141592); // 소수점 둘째자리까지만 반올림해서 출력 

    // %g : 실수 (길면 지수 형태로도 출력)
    printf("%g\n", 3.141592);
    printf("%.3g\n", 873616262.12345602344);

    // %c : 문자 출력 (알파벳, 숫자, 몇몇 기호, \n)
    printf("%c %c %c\n", 'a', 'b', 'c');

    printf("%s\n", "안녕하세요");
}

